//variable: A name which can store a value

/*variable rules
 *    1)variable name must contain
 *        A-Z/a-z,digits(0-9) and underscore(_)
 *    2) variable does not start with digits
 *        e.g. 0name 
 *    3)A reserve word or a keyword cannot use as a variable name
 *        e.g. var for x
 *    4)we can not use whitespace in variable name 
 * 
 *        e.g. var first name x 
 */